package day18_loops;

public class while_practice1 {
    public static void main(String[] args) {


    /*
    write a program that will add all the numbers from 1 - 500
     */
        int i = 500;
        int i2 = 0;

        while (++i2 <= i) {
            System.out.print(i2 + " ");
        }
    }
}
